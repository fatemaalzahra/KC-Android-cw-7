package com.example.cw7;

public class Soso {

    private String SS1;
    private Integer SS2;
    private Integer SS3;

    public String getSS1() {
        return SS1;
    }

    public void setSS1(String SS1) {
        this.SS1 = SS1;
    }

    public Integer getSS2() {
        return SS2;
    }

    public void setSS2(Integer SS2) {
        this.SS2 = SS2;
    }

    public Integer getSS3() {
        return SS3;
    }

    public void setSS3(Integer SS3) {
        this.SS3 = SS3;
    }

    public Soso(String SS1, Integer SS2, Integer SS3) {
        this.SS1 = SS1;
        this.SS2 = SS2;
        this.SS3 = SS3;
    }
}
